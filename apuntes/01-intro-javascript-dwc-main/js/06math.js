// math

const number = 1.253;
const number2 = 1.89;
const number3 = -5;
console.log(Math.PI); // genera numero pi
console.log(Math.round(number)); // redondeo normal
console.log(Math.round(number2));
console.log(Math.ceil(number)); // redondeo hacia arriba
console.log(Math.floor(number2)); // redondo hacia abajo
console.log(Math.abs(number3)); // valor absoluto
console.log(Math.pow(number3, 2)); // potencia
console.log(Math.sqrt(144)); // raiz cuadrada
console.log(Math.random()); // genera un numero random entre el 0 y 1
console.log(Math.ceil(Math.random() * 30)); // aleatorio dentro de un rango, entre 0 y 30
console.log(Math.min(1, 2, 3, 4, 5, 6)); // minimo
console.log(Math.max(1, 2, 3, 4, 5, 6)); // maximo
